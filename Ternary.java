class Ternary{
    public static void main(String args[]){
        int a=10;
        int b=30;
        int c=40;
        int res=(a<b)? a:b;
        System.out.println(res);
        String result=(a<c) ? "a is less" : "c is less";
        System.out.println(result);

        //this is a ternary program

    }
}